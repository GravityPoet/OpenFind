import AppKit
import Foundation
import Observation

@MainActor
@Observable
final class ClipboardHistoryStore {
    static let maximumEntries = 1_000
    static let maximumItemBytes = 16 * 1_024 * 1_024
    static let maximumHistoryBytes = 48 * 1_024 * 1_024
    static let defaultHistoryLimit = 500
    static let defaultItemLimitBytes = 8 * 1_024 * 1_024
    private static let persistenceEnabledKey = "OpenFind.clipboardPersistenceEnabledV1"
    private static let historyLimitKey = "OpenFind.clipboardHistoryLimitV1"
    private static let itemLimitKey = "OpenFind.clipboardItemLimitBytesV1"
    private static let ignoredAppsKey = "OpenFind.clipboardIgnoredAppsV1"
    private static let pasteAutomaticallyKey = "OpenFind.clipboardPasteAutomaticallyV1"
    private static let pasteWithoutFormattingKey = "OpenFind.clipboardPasteWithoutFormattingV1"
    private static let clearHistoryOnQuitKey = "OpenFind.clipboardClearHistoryOnQuitV1"
    private static let clearSystemClipboardOnQuitKey = "OpenFind.clipboardClearSystemClipboardOnQuitV1"
    private static let fuzzySearchKey = "OpenFind.clipboardFuzzySearchV1"
    private static let remoteClipboardType = "com.apple.is-remote-clipboard"
    private static let ignoredTypes: Set<String> = [
        "org.nspasteboard.ConcealedType",
        "org.nspasteboard.TransientType",
        "org.nspasteboard.AutoGeneratedType",
        "com.agilebits.onepassword",
        "com.agilebits.onepassword.password",
    ]
    private static let supportedTypes: Set<String> = [
        NSPasteboard.PasteboardType.string.rawValue,
        "public.utf8-plain-text",
        "public.utf16-external-plain-text",
        "public.rtf",
        "public.html",
        "public.url",
        "public.file-url",
        "public.png",
        "public.tiff",
        "public.jpeg",
    ]

    @ObservationIgnored private let defaults: UserDefaults
    @ObservationIgnored private let persistence: any ClipboardHistoryPersisting
    @ObservationIgnored private let pasteboard: NSPasteboard
    private(set) var entries: [ClipboardEntry] = []
    private(set) var lastErrorMessage: String?
    private(set) var isPersistenceEnabled = true
    private(set) var historyLimit: Int
    private(set) var itemLimitBytes: Int
    private(set) var ignoredBundleIdentifiers: Set<String>
    private(set) var pasteAutomatically: Bool
    private(set) var pasteWithoutFormatting: Bool
    private(set) var clearHistoryOnQuit: Bool
    private(set) var clearSystemClipboardOnQuit: Bool
    private(set) var fuzzySearchEnabled: Bool
    var query = ""
    var selectedIndex = 0

    init(
        defaults: UserDefaults = .standard,
        persistence: any ClipboardHistoryPersisting = EncryptedClipboardHistoryPersistence(),
        pasteboard: NSPasteboard = .general
    ) {
        self.defaults = defaults
        self.persistence = persistence
        self.pasteboard = pasteboard
        let persistenceEnabled = defaults.object(forKey: Self.persistenceEnabledKey) as? Bool ?? true
        self.isPersistenceEnabled = persistenceEnabled
        let storedHistoryLimit = defaults.object(forKey: Self.historyLimitKey) as? Int
            ?? Self.defaultHistoryLimit
        historyLimit = min(Self.maximumEntries, max(10, storedHistoryLimit))
        let storedItemLimit = defaults.object(forKey: Self.itemLimitKey) as? Int
            ?? Self.defaultItemLimitBytes
        itemLimitBytes = min(Self.maximumItemBytes, max(1_024, storedItemLimit))
        ignoredBundleIdentifiers = Set(
            defaults.stringArray(forKey: Self.ignoredAppsKey) ?? []
        )
        pasteAutomatically = defaults.object(forKey: Self.pasteAutomaticallyKey) as? Bool ?? true
        pasteWithoutFormatting = defaults.bool(forKey: Self.pasteWithoutFormattingKey)
        clearHistoryOnQuit = defaults.bool(forKey: Self.clearHistoryOnQuitKey)
        clearSystemClipboardOnQuit = defaults.bool(forKey: Self.clearSystemClipboardOnQuitKey)
        fuzzySearchEnabled = defaults.bool(forKey: Self.fuzzySearchKey)
        if persistenceEnabled {
            do {
                entries = Array(try persistence.load().prefix(Self.maximumEntries))
                trimToLimits()
            } catch {
                entries = []
                lastErrorMessage = error.localizedDescription
            }
        } else {
            entries = []
        }
    }

    var filteredEntries: [ClipboardEntry] {
        let normalizedQuery = query.trimmingCharacters(in: .whitespacesAndNewlines)
            .localizedLowercase
        let sorted = entries.sorted { lhs, rhs in
            if lhs.isPinned != rhs.isPinned { return lhs.isPinned }
            return lhs.createdAt > rhs.createdAt
        }
        guard !normalizedQuery.isEmpty else { return sorted }
        if fuzzySearchEnabled {
            return sorted
                .compactMap { entry -> (entry: ClipboardEntry, score: Int)? in
                    guard let score = fuzzyScore(
                        query: normalizedQuery,
                        candidate: entry.previewText.localizedLowercase
                    ) else { return nil }
                    return (entry, score)
                }
                .sorted { lhs, rhs in
                    if lhs.entry.isPinned != rhs.entry.isPinned { return lhs.entry.isPinned }
                    if lhs.score != rhs.score { return lhs.score > rhs.score }
                    return lhs.entry.createdAt > rhs.entry.createdAt
                }
                .map(\.entry)
        }
        return sorted.filter { entry in
            entry.previewText.localizedLowercase.contains(normalizedQuery)
        }
    }

    var selectedEntry: ClipboardEntry? {
        let visible = filteredEntries
        guard visible.indices.contains(selectedIndex) else { return nil }
        return visible[selectedIndex]
    }

    func setPersistenceEnabled(_ enabled: Bool) {
        isPersistenceEnabled = enabled
        defaults.set(enabled, forKey: Self.persistenceEnabledKey)
        guard !enabled else {
            persist()
            return
        }
        do {
            try persistence.remove()
        } catch {
            lastErrorMessage = error.localizedDescription
        }
    }

    @discardableResult
    func captureCurrentPasteboard(
        sourceBundleIdentifier: String? = nil,
        sourceIdentifiers: Set<String> = []
    ) -> Bool {
        var identifiers = sourceIdentifiers
        if let sourceBundleIdentifier { identifiers.insert(sourceBundleIdentifier) }
        identifiers = Set(identifiers.map {
            $0.trimmingCharacters(in: .whitespacesAndNewlines)
        })
        if identifiers.contains(Bundle.main.bundleIdentifier ?? "")
            || identifiers.contains(where: { ignoredBundleIdentifiers.contains($0) }) {
            return false
        }
        guard let item = pasteboard.pasteboardItems?.first else { return false }
        let types = Set(item.types.map(\.rawValue))
        guard !types.contains(Self.remoteClipboardType),
              types.isDisjoint(with: Self.ignoredTypes) else { return false }
        var representations: [String: Data] = [:]
        var totalBytes = 0
        for type in item.types where Self.supportedTypes.contains(type.rawValue) {
            guard let data = item.data(forType: type), !data.isEmpty else { continue }
            totalBytes += data.count
            guard totalBytes <= itemLimitBytes else {
                lastErrorMessage = ClipboardHistoryError.contentTooLarge.localizedDescription
                return false
            }
            representations[type.rawValue] = data
        }
        guard let content = makeContent(representations: representations) else {
            lastErrorMessage = ClipboardHistoryError.unsupportedContent.localizedDescription
            return false
        }
        return ingest(
            representations: content.representations,
            previewText: content.previewText,
            kind: content.kind
        )
    }

    @discardableResult
    func ingest(
        representations: [String: Data],
        previewText: String,
        kind: ClipboardEntryKind,
        createdAt: Date = Date()
    ) -> Bool {
        let totalBytes = representations.values.reduce(0) { $0 + $1.count }
        guard !representations.isEmpty else { return false }
        guard totalBytes <= itemLimitBytes else {
            lastErrorMessage = ClipboardHistoryError.contentTooLarge.localizedDescription
            return false
        }
        let retainedEntryID: UUID
        if let duplicateIndex = entries.firstIndex(where: {
            $0.representations == representations
        }) {
            var duplicate = entries.remove(at: duplicateIndex)
            duplicate.createdAt = createdAt
            duplicate.previewText = String(previewText.prefix(4_096))
            duplicate.kind = kind
            retainedEntryID = duplicate.id
            entries.insert(duplicate, at: 0)
        } else {
            let entry = ClipboardEntry(
                createdAt: createdAt,
                previewText: String(previewText.prefix(4_096)),
                kind: kind,
                representations: representations
            )
            retainedEntryID = entry.id
            entries.insert(entry, at: 0)
        }
        trimToLimits()
        guard entries.contains(where: { $0.id == retainedEntryID }) else {
            lastErrorMessage = ClipboardHistoryError.historyFull.localizedDescription
            return false
        }
        selectedIndex = 0
        persist()
        return true
    }

    func copy(_ entry: ClipboardEntry, plainTextOnly: Bool = false) throws {
        guard entries.contains(where: { $0.id == entry.id }) else {
            throw ClipboardHistoryError.entryNotFound
        }
        if plainTextOnly {
            guard let text = plainText(for: entry) else {
                throw ClipboardHistoryError.unsupportedContent
            }
            pasteboard.clearContents()
            guard pasteboard.setString(text, forType: .string) else {
                throw ClipboardHistoryError.pasteboardWriteFailed
            }
            return
        }
        let item = NSPasteboardItem()
        for (type, data) in entry.representations {
            item.setData(data, forType: NSPasteboard.PasteboardType(rawValue: type))
        }
        pasteboard.clearContents()
        guard pasteboard.writeObjects([item]) else {
            throw ClipboardHistoryError.pasteboardWriteFailed
        }
    }

    func canCopyPlainText(_ entry: ClipboardEntry) -> Bool {
        plainText(for: entry) != nil
    }

    func setHistoryLimit(_ limit: Int) {
        let normalized = min(Self.maximumEntries, max(10, limit))
        historyLimit = normalized
        defaults.set(normalized, forKey: Self.historyLimitKey)
        if unpinnedEntryCount > normalized {
            trimToLimits()
            selectedIndex = min(selectedIndex, max(0, filteredEntries.count - 1))
            persist()
        }
    }

    func setItemLimitMegabytes(_ megabytes: Int) {
        let bytes = megabytes * 1_024 * 1_024
        itemLimitBytes = min(Self.maximumItemBytes, max(1_024, bytes))
        defaults.set(itemLimitBytes, forKey: Self.itemLimitKey)
    }

    func setIgnoredBundleIdentifiers(_ identifiers: Set<String>) {
        ignoredBundleIdentifiers = Set(identifiers.compactMap { value in
            let normalized = value.trimmingCharacters(in: .whitespacesAndNewlines)
            guard !normalized.isEmpty,
                  normalized.utf8.count <= 512,
                  normalized.unicodeScalars.allSatisfy({
                      !CharacterSet.controlCharacters.contains($0)
                  }) else { return nil }
            return normalized
        }.prefix(128))
        defaults.set(ignoredBundleIdentifiers.sorted(), forKey: Self.ignoredAppsKey)
    }

    func setPasteAutomatically(_ enabled: Bool) {
        pasteAutomatically = enabled
        defaults.set(enabled, forKey: Self.pasteAutomaticallyKey)
    }

    func setPasteWithoutFormatting(_ enabled: Bool) {
        pasteWithoutFormatting = enabled
        defaults.set(enabled, forKey: Self.pasteWithoutFormattingKey)
    }

    func setClearHistoryOnQuit(_ enabled: Bool) {
        clearHistoryOnQuit = enabled
        defaults.set(enabled, forKey: Self.clearHistoryOnQuitKey)
    }

    func setClearSystemClipboardOnQuit(_ enabled: Bool) {
        clearSystemClipboardOnQuit = enabled
        defaults.set(enabled, forKey: Self.clearSystemClipboardOnQuitKey)
    }

    func setFuzzySearchEnabled(_ enabled: Bool) {
        fuzzySearchEnabled = enabled
        defaults.set(enabled, forKey: Self.fuzzySearchKey)
        selectedIndex = min(selectedIndex, max(0, filteredEntries.count - 1))
    }

    /// Applies the user's quit policy only after the application has received
    /// a final termination approval. This is intentionally separate from
    /// `ClipboardController.stop()`, because a cancelled termination must not
    /// erase history or the current system clipboard.
    func prepareForTermination() {
        if clearHistoryOnQuit { clearAll() }
        if clearSystemClipboardOnQuit { pasteboard.clearContents() }
    }

    func delete(_ entry: ClipboardEntry) {
        entries.removeAll { $0.id == entry.id }
        selectedIndex = min(selectedIndex, max(0, filteredEntries.count - 1))
        persist()
    }

    func togglePinned(_ entry: ClipboardEntry) {
        guard let index = entries.firstIndex(where: { $0.id == entry.id }) else { return }
        entries[index].isPinned.toggle()
        persist()
    }

    func clearUnpinned() {
        entries.removeAll { !$0.isPinned }
        selectedIndex = 0
        persist()
    }

    func clearAll() {
        entries.removeAll()
        selectedIndex = 0
        persist()
    }

    func clearError() {
        lastErrorMessage = nil
    }

    /// Publishes a safe, localized error for the UI without exposing clipboard
    /// payloads or arbitrary system error strings.
    func reportError(_ error: Error) {
        if let error = error as? LocalizedError,
           let description = error.errorDescription,
           !description.isEmpty {
            lastErrorMessage = description
        } else {
            lastErrorMessage = L("Clipboard Operation Failed")
        }
    }

    private func persist() {
        guard isPersistenceEnabled else { return }
        do {
            try persistence.save(entries)
            lastErrorMessage = nil
        } catch {
            lastErrorMessage = error.localizedDescription
        }
    }

    private func trimToLimits() {
        var payloadBytes = retainedPayloadBytes
        while entries.count > Self.maximumEntries || payloadBytes > Self.maximumHistoryBytes {
            guard let index = entries.lastIndex(where: { !$0.isPinned }) ?? entries.indices.last else {
                break
            }
            payloadBytes -= payloadByteCount(of: entries[index])
            entries.remove(at: index)
        }
        while unpinnedEntryCount > historyLimit {
            guard let index = entries.lastIndex(where: { !$0.isPinned }) else { break }
            entries.remove(at: index)
        }
    }

    private var retainedPayloadBytes: Int {
        entries.reduce(0) { $0 + payloadByteCount(of: $1) }
    }

    private var unpinnedEntryCount: Int {
        entries.lazy.filter { !$0.isPinned }.count
    }

    private func payloadByteCount(of entry: ClipboardEntry) -> Int {
        entry.representations.values.reduce(0) { $0 + $1.count }
    }

    private func makeContent(
        representations: [String: Data]
    ) -> (representations: [String: Data], previewText: String, kind: ClipboardEntryKind)? {
        let keys = Set(representations.keys)
        if let plain = representations[NSPasteboard.PasteboardType.string.rawValue]
            ?? representations["public.utf8-plain-text"],
           let text = String(data: plain, encoding: .utf8) {
            let kind: ClipboardEntryKind = keys.contains("public.rtf") || keys.contains("public.html")
                ? .richText : .text
            return (representations, text, kind)
        }
        if let urlData = representations["public.url"],
           let url = URL(dataRepresentation: urlData, relativeTo: nil) {
            return (representations, url.absoluteString, .url)
        }
        if let fileData = representations["public.file-url"],
           let url = URL(dataRepresentation: fileData, relativeTo: nil) {
            return (representations, url.lastPathComponent, .file)
        }
        if keys.contains("public.png") || keys.contains("public.tiff") || keys.contains("public.jpeg") {
            return (representations, "Image", .image)
        }
        return representations.isEmpty ? nil : (representations, "Clipboard item", .other)
    }

    private func plainText(for entry: ClipboardEntry) -> String? {
        if let data = entry.representations[NSPasteboard.PasteboardType.string.rawValue]
            ?? entry.representations["public.utf8-plain-text"],
           let text = String(data: data, encoding: .utf8) {
            return text
        }
        if let data = entry.representations["public.utf16-external-plain-text"],
           let text = String(data: data, encoding: .utf16) {
            return text
        }
        if entry.kind == .url { return entry.previewText }
        return nil
    }

    private func fuzzyScore(query: String, candidate: String) -> Int? {
        guard !query.isEmpty else { return 0 }
        var queryIndex = query.startIndex
        var candidateIndex = candidate.startIndex
        var score = 0
        var previousMatch: String.Index?
        while queryIndex < query.endIndex {
            var found: String.Index?
            while candidateIndex < candidate.endIndex {
                if candidate[candidateIndex] == query[queryIndex] {
                    found = candidateIndex
                    break
                }
                candidateIndex = candidate.index(after: candidateIndex)
            }
            guard let match = found else { return nil }
            // Consecutive and word-boundary matches rank above scattered
            // matches while retaining deterministic date ordering for ties.
            score += previousMatch.map {
                candidate.distance(from: $0, to: match) == 1 ? 4 : 1
            } ?? (match == candidate.startIndex ? 5 : 2)
            previousMatch = match
            candidateIndex = candidate.index(after: match)
            queryIndex = query.index(after: queryIndex)
        }
        return score
    }
}
